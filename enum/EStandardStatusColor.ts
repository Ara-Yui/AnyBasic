/**
 * 标准状态色枚举
 */
export enum EStandardStatusColor {
  /**
   * 警告色
   * @description 对应#faad14
   */
  Warning = '#faad14',
  
  /**
   * 错误色
   * @description 对应#f5222d
   */
  Error = '#f5222d',

  /**
   * 成功色
   * @description 对应#52c41a
   */
  Success = '#52c41a',

  /**
   * 失败色
   * @description 对应#f5222d
   */
  Failed = '#f5222d',

  /**
   * 默认色
   * @description 对应#1890ff
   */
  Default = '#1890ff',
}